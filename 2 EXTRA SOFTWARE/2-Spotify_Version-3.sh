echo "# Author 	: 	Peter Zehnder                                                                                       "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# RUN AT YOUR OWN RISK                                                                                            "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Adding The Spotify Repository Signing Keys To Be Able To Verify Downloaded Packages                             "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-key adv --keyserver hkp://keyserver.ubuntu.com:80 --recv-keys 0DF731E45CE24F27EEEB1450EFDC8610341D9410

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Adding The Spotify Repository                                                                                   "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo deb http://repository.spotify.com stable non-free | sudo tee /etc/apt/sources.list.d/spotify.list

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Update The List Of Available Packages                                                                           "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get update -y

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Install Spotify                                                                                                 "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get install -y spotify-client

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Spotify Installed                                                                                               "
echo "#                                                                                                                 "
echo "##################################################################################################################"

