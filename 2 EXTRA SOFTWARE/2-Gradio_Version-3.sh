set -e

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Written To Be Used On 64 Bits Computers                                                                         "
echo "# Author 	: 	Peter Zehnder                                                                                       "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# RUN AT YOUR OWN RISK                                                                                            "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Install Flatpack                                                                                                "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get install -y flatpak

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Add Flathub Remote                                                                                              "
echo "#                                                                                                                 "
echo "##################################################################################################################"

flatpak remote-add --if-not-exists flathub https://flathub.org/repo/flathub.flatpakrepo

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Install Gradio From Flathub                                                                                     "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo flatpak install flathub de.haeckerfelix.gradio

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Gradio Installed                                                                                                "
echo "#                                                                                                                 "
echo "##################################################################################################################"

