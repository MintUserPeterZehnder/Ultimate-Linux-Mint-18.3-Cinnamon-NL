#!/bin/bash
set -e

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Written To Be Used On 64 Bits Computers                                                                         "
echo "# Author 	: 	Peter Zehnder                                                                                       "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# RUN AT YOUR OWN RISK                                                                                            "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Install The GPG Key                                                                                             "
echo "#                                                                                                                 "
echo "##################################################################################################################"

wget -qO - https://download.sublimetext.com/sublimehq-pub.gpg | sudo apt-key add -

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Ensure apt Is Set Up To Work With https Sources                                                                 "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get install -y apt-transport-https

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Select The Channel To Use: Stable                                                                               "
echo "#                                                                                                                 "
echo "##################################################################################################################"

wget https://download.sublimetext.com/ apt/stable/ | sudo tee /etc/apt/sources.list.d/sublime-text.list

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Update apt Sources And Install Sublime Text                                                                     "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get update -y
sudo apt-get install -y sublime-text

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Sublime Text Installed                                                                                          "
echo "#                                                                                                                 "
echo "##################################################################################################################"

