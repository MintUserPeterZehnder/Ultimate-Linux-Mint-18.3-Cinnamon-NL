#!/bin/bash
set -e

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Written To Be Used On 64 Bits Computers                                                                         "
echo "# Author 	: 	Peter Zehnder                                                                                       "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# RUN AT YOUR OWN RISK                                                                                            "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 " 
echo "# Dependencies                                                                                                    "
echo "#                                                                                                                 "
echo "##################################################################################################################"

wget security.debian.org/debian-security/pool/updates/main/libg/libgcrypt11/libgcrypt11_1.5.0-5+deb7u6_amd64.deb

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Install Dependencies                                                                                            "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get -f install -y
sudo dpkg -i libgcrypt11_1.5.0-5+deb7u6_amd64.deb

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Download Brackets                                                                                               "
echo "#                                                                                                                 "
echo "##################################################################################################################"

wget https://github.com/adobe/brackets/releases/download/release-1.12/Brackets.Release.1.12.64-bit.deb

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Install Brackets                                                                                                "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get -f install -y
sudo dpkg -i Brackets.Release.1.12.64-bit.deb

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Delete Downloads                                                                                                "
echo "#                                                                                                                 "
echo "##################################################################################################################"

rm Brackets.Release.1.12.64-bit.deb
rm libgcrypt11_1.5.0-5+deb7u6_amd64.deb

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Brackets Installed                                                                                              "
echo "#                                                                                                                 "
echo "##################################################################################################################"

