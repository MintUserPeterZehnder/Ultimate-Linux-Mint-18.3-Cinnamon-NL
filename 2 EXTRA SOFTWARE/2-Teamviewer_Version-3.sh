#!/bin/bash
set -e

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Written To Be Used On 64 Bits Computers                                                                         "
echo "# Author 	: 	Peter Zehnder                                                                                       "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# RUN AT YOUR OWN RISK                                                                                            "
echo "#                                                                                                                 "
echo "##################################################################################################################"

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Download Latest Version Of Teamviewer                                                                           "
echo "#                                                                                                                 "
echo "##################################################################################################################"

wget https://download.teamviewer.com/download/teamviewer_i386.deb -O /tmp/teamviewer_i386.deb

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Install Dependencies                                                                                            "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get -f -y install
sudo apt install -y libjpeg62:i386

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Install Teamviewer                                                                                              "
echo "#                                                                                                                 "
echo "##################################################################################################################"

sudo apt-get -f -y install
sudo dpkg -i /tmp/teamviewer_i386.deb

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Clean Up                                                                                                        "
echo "#                                                                                                                 "
echo "##################################################################################################################"

rm /tmp/teamviewer_i386.deb

echo "##################################################################################################################"
echo "#                                                                                                                 "
echo "# Teamviewer Installed                                                                                            "
echo "#                                                                                                                 "
echo "##################################################################################################################"

